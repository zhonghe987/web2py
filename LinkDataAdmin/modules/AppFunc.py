def getStatusButton(status):
    if status == 'run':
        s = '暂停'
    elif status == 'stop':
        s = '启动'
    else:
        s = ''
    return s

def getStatus(status):
    if status=='run':    
        s='正常'
    elif status=='stop':    
        s='暂停' 
    elif status == "no": 
        s='未选中'
    elif status == "yes":
        s = '选中'
    else:
        s = ''
    return s

def getTaskStatus(status):
    if status == 'QUEUED':
        s = '在队列中等待'
    elif status == 'ASSIGNED':
        s = '分配中'
    elif status == 'EXPIRED':
        s = '过期'
    elif status == 'RUNNING':
        s = '正在运行'
    elif status == 'TIMEOUT':
        s = '运行超时'
    elif status == 'FAILED':
        s = '运行失败'
    elif status == 'COMPLETED':
        s = '运行完成'
    else:
        s = ''
    return s

